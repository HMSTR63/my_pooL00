char *ft_strcat(char *dest, char *src)
{
    int h;
    int m;

    h = 0;
    m = 0;
    while (dest[h] != '\0')
        h++;
    while (src[m] != '\0')
    {
        dest[h] = src[m];
        m++;
        h++;
    }
    dest[h] = '\0';
    return (dest);
}
