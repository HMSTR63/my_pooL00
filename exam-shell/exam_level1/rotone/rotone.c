#include <unistd.h>

int main(int ac, char **av)
{
    int i;

    i = 0;
    if(ac == 2)
    {
        while(av[1][i] != '\0')
        {
            if(av[1][i] >= 'a' && 'y' >= av[1][i] || av[1][i] >= 'A' && 'Y' >= av[1][i])
                av[1][i] = (av[1][i] + 1);
            else if(av[1][i] == 'z' || av[1][i] == 'Z')
                av[1][i] = (av[1][i] - 25);
            write(1, &av[1][i], 1);
            i++;
        }
        write(1, "\n", 1);
    }
    else 
        write(1, "\n", 1);
    return 0;
}
